import nltk
from nltk.stem import PorterStemmer
from nltk.stem import WordNetLemmatizer
from nltk.corpus import wordnet
from nltk.tokenize import word_tokenize

nltk.download('punkt')
nltk.download('wordnet')


text = "Today we are talking about a man was running in his backyard , and then a lady came and slapped on his face ."

words = word_tokenize(text)


porter_stemmer = PorterStemmer()
wordnet_lemmatizer = WordNetLemmatizer()


stemmed_words = [porter_stemmer.stem(word) for word in words]


lemmatized_words = [wordnet_lemmatizer.lemmatize(word, pos=wordnet.VERB) for word in words]

print("Original text:", text)
print("\nStemmed text:", " ".join(stemmed_words))
print("\nLemmatized text:", " ".join(lemmatized_words))